# # Defining a Class
class car:
	carColor = "White"
	carModel = "ABC2011"
	carGears = 5
	carSeats = 4
	carName = "HondaXYZ"


# Making an Object of the Class
obj1 = car()

# Accessing class characteristics / properties
print(obj1.carName)
print(obj1.carModel)
print(obj1.carColor)
print(obj1.carSeats)
print(obj1.carGears)